package com.cyj.pojo;

public class WxTemplateValue {

    private String value;

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return "WxTemplateValue{" +
                "value='" + value + '\'' +
                '}';
    }
}
